tasks.register("makeCerts") {
    exec {
        fun shell(line: String) = commandLine("/bin/bash", "-c", line)
        shell("openssl genrsa -out secrets/private_key.pem 2048")
        shell("openssl pkcs8 -topk8 -nocrypt -in secrets/private_key.pem -out secrets/private_key_pk8.der -outform DER")
        shell("openssl rsa -pubout -in secrets/private_key.pem -out src/main/resources/public_key_pk8.der -outform DER")
    }
}
